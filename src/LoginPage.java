    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String url = "jdbc:mysql://localhost:3306/library?zeroDateTimeBehavior=CONVERT_TO_NULL"; // JDBC URL for MySQL connection
    String mySQLname = "root"; // MySQL username
    String mySQLpass = "root"; // MySQL password
    String getPass = new String(password.getPassword()); // Getting password from password field
    String username = user.getText(); // Getting username from text field
    String query = "SELECT PASSWORD FROM admin WHERE USER_ID='" + username + "'"; // SQL query to retrieve password
    
    try {
        // Establishing database connection
        Connection connect = DriverManager.getConnection(url, mySQLname, mySQLpass);
        
        // Creating statement
        Statement stm = connect.createStatement();
        
        // Executing query
        ResultSet rs = stm.executeQuery(query);
        
        if (rs.next()) {
            String realpass = rs.getString("PASSWORD");
            if (realpass.equals(getPass)) {
                // Passwords match, open the dashboard
                Dashboard dash = new Dashboard();
                dash.setVisible(true);
            } else {
                // Incorrect password
                JOptionPane.showMessageDialog(this, "Incorrect username or password");
            }
        } else {
            // User not found
            JOptionPane.showMessageDialog(this, "User not found");
        }
        
        // Closing resources
        rs.close();
        stm.close();
        connect.close();
    } catch (SQLException e) {
        // Exception handling
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_jButton1ActionPerformed

